                                <h2>About Me</h2>
                                <p>When my grandfather died in 1992, he left a house in the countryside to my parents. They never arranged any upkeep for it and so the house was left idle for over a decade. My grandfather was an original thinker, an inventor and a musician. He spoke little and preferred being on his own than with friends.

                                Travelling with my sister, we decided to eventually visit the house as one winter was ending. The house had been frozen in time for 10 years but for the gathering dust that was smothering everything. Walking slowly through the hall, we made our way to the only room he really lived in, his workshop. Staring into a half-lit corner, we were scared by a lifelike silhouette. We thought someone was here in the middle of the room. It was not moving, and we soon figured out it the object was a project of our grandfather. An automaton and it seemed to be still in good condition. We didn’t yet know its purpose.</p>
                                <p>After a year or so working on it during the weekends, we finally managed to bring it back to life. This automaton was weird, a real human body that had been preserved and treated to stop it from aging and a TV set from the 60s held the place of the head. We started the automaton and stepping back immediately, we feared what it could do. The automaton started moving in a strange way. It was moving its hands and fingers like something was missing. We soon found out it was playing air guitar when my sister recognized the position of a chord and then another. Putting my old classic guitar into its embrace, something magical happened. It was flamboyant, playing guitar for us. Then to our true astonishment, the TV screen lit up, showing mysterious films of old. A mixture of personal films with a poetic twist and cuts of movies synced to the mirror man’s musicianship and energy.
                                The purpose and mysteries of the Flamboyant Mirror Man must now be shown to everybody with an eye for wonder. We only hope our grandfather’s creation will be appreciated.</p>
